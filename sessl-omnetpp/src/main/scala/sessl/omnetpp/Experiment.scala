  def executeExperiment(): Unit = {
    //TODO
  }

  /** Alias for brevity. */
  private[this] def write(content: String): Unit = fileWriter.get.write(content + '\n')

  /** Alias for brevity. */
  private[this] def write(key: String, value: String): Unit = write(key + " = " + value)
}